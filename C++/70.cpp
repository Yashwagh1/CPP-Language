// STL = Container + Algorithm + Iterators
/*

Container :- the object which stores the data
Algorithm :- Algorithm is proceduce to process data
Iterators :- Object which point to an element ofs container */


// TYPE OF THE CONTAINERS IN STL
/*
1. Sequece Container        store the data in linear fashion    ex.vector,list,dequeue
2. Associative Container    Direct access       ex.set/multiset,maps/multimaps
3. Derived Container        real world modeling     ex.stack,queue,priority-queue
*/